// IMPRIMIR ENTIDADES EN LAS TABLAS

const allSymbols = document.querySelectorAll(".symbol");
const allDecimals = document.querySelectorAll(".decimal");
const allNumbers = document.querySelectorAll(".number");
const allEntities = document.querySelectorAll(".entity");

allSymbols.forEach((symbol, symbol_index) => {
    for (let key in decimals){
        if (symbol.textContent == key){
            allDecimals.forEach((decimal, decimal_index) => {
                if(symbol_index == decimal_index){
                    decimal.textContent = `${decimals[key]}`
                }
            })
        }
    }
    for (let key in numbers){
        if (symbol.textContent == key){
            allNumbers.forEach((number, number_index) => {
                if (symbol_index == number_index){
                    number.textContent = `${numbers[key]}`
                }
            })
        }
    }
    for (let key in entities){
        if (symbol.textContent == key){
            allEntities.forEach((entity, entity_index) => {
                if (symbol_index == entity_index){
                    entity.textContent = `${entities[key]}`
                }
            })
        }
    }  
})
